# entrada da temperatura em Celsius
celsius = float(input('Temperatura em Celsius: '))

# conversão para Fahrenheit e exibição
fahr = 9*celsius/5 + 32
print('%.2f em Celsius corresponde a %.2f em Fahrenheit' % (celsius, fahr))
